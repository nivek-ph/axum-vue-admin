function readLocale(): Locale {
  if (typeof localStorage === 'undefined') return 'en-US';
  return localStorage.getItem(STORAGE_KEY) === 'zh-CN' ? 'zh-CN' : 'en-US';
}

export const currentLocale = ref<Locale>(readLocale());
export const isEnglish = computed(() => currentLocale.value === 'en-US');

export function setLocale(locale: Locale) {
  currentLocale.value = locale;
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem(STORAGE_KEY, locale);
  }
}

export function toggleLocale() {
  setLocale(currentLocale.value === 'zh-CN' ? 'en-US' : 'zh-CN');
}

export function t(source: string, vars: Record<string, string | number> = {}) {
  const template = currentLocale.value === 'zh-CN' ? zh[source] || source : source;
  return Object.entries(vars).reduce((text, [key, value]) => text.replaceAll(`{${key}}`, String(value)), template);
}

export const I18nPlugin = {
  install(app: App) {
    app.config.globalProperties.$t = t;
    app.config.globalProperties.$locale = currentLocale;
    app.config.globalProperties.$setLocale = setLocale;
  },
};

declare module 'vue' {
  interface ComponentCustomProperties {
    $t: typeof t;
    $locale: typeof currentLocale;
    $setLocale: typeof setLocale;
  }
}
